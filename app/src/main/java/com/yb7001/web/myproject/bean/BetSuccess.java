package com.yb7001.web.myproject.bean;



public class BetSuccess {
    private String betId;
    private String betNum;
    private String betGame;
    private String betOdd;
    private String betMoney;

    public String getBetId() {
        return betId;
    }

    public void setBetId(String betId) {
        this.betId = betId;
    }

    public String getBetNum() {
        return betNum;
    }

    public void setBetNum(String betNum) {
        this.betNum = betNum;
    }

    public String getBetGame() {
        return betGame;
    }

    public void setBetGame(String betGame) {
        this.betGame = betGame;
    }

    public String getBetOdd() {
        return betOdd;
    }

    public void setBetOdd(String betOdd) {
        this.betOdd = betOdd;
    }

    public String getBetMoney() {
        return betMoney;
    }

    public void setBetMoney(String betMoney) {
        this.betMoney = betMoney;
    }
}
